package primitives;

public class Point2D {
		private Coordinate _x;
		private Coordinate _y;
		public Coordinate getX()
		{
			return _x;
		}
		public void setX(Coordinate x)
		{
			_x = x;
		}
		public Coordinate getY()
		{
			return _y;
		}
		public void setY(Coordinate y)
		{
			_y = y;
		}
	}

